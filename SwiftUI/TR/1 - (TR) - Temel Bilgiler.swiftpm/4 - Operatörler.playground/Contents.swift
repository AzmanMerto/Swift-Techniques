import UIKit

//MARK: - Operatörler ( = , + , - , * , / , % )
/// Çoğunu ilk okulda öğrendiğinizi simgelerdir. Yazılımda çoğu işlemi öğrendiğimiz işlem ile aynı. Bazı farklılıklar var. Alttaki örnekler ile bu operatörlerin işlevini anlatacağım.


var eşitleme = 10

eşitleme = 5 // Burada sayının değerini farklı bir değer atıyoruz.
print("Eşitleme : \(eşitleme)")

var toplama = 10

toplama + 5 // Burada sayıyı 5 ile topluyoruz.
print("Toplama : \(toplama)")

var çıkarma = 10

çıkarma - 5 // Burada sayıyı 5 ile çıkarıyoruz.
print("Çıkarma : \(çıkarma)")

var çarpma = 10

çarpma * 2 // Burada sayıyı 2 ile çarpıyoruz
print("Çarpma : \(çarpma)")

var bölme = 10

bölme / 2 // Burada sayıyı 2 ile bölüyoruz.
print("Bölme : \(bölme)")

var yüzdelik = 11

yüzdelik % 2 // Burada kalan sayıyı gösteriyoruz.
print("Yüzdelik :\(yüzdelik)")

